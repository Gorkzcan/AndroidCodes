package com.gorkemozcan.oopproject;

public class Mathematics {

    //Static polymorphism
    /*
    3 methodun adı aynı ama yapacakları işlemler ayrı
     */
    public int sum()
    {
        return 0;
    }
    public int sum(int x, int y)
    {
        return x + y;
    }
    public int sum(int x, int y, int z)
    {
        return x + y + z;
    }

}
